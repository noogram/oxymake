//! # OxyMake
//!
//! This crate reserves the `oxymake` name on crates.io. It contains no
//! functionality.
//!
//! OxyMake is a formally-specified, content-addressable workflow engine,
//! distributed as a single static binary named `ox`.
//!
//! ## Installing the `ox` binary
//!
//! Prebuilt binaries for Linux and macOS are attached to each GitHub Release:
//! <https://github.com/noogram/oxymake/releases>
//!
//! Or build from source:
//!
//! ```text
//! cargo install --git https://github.com/noogram/oxymake ox-cli
//! ```
//!
//! Documentation: <https://noogram.org/oxymake>
