    #[test]
    fn test_generate_run_block_backslashes_survive_toml() {
        // Python code with escape sequences must round-trip through TOML
        // verbatim — a basic """ string would interpret `\n` at parse time.
        let mut rule = make_rule("analyze");
        rule.execution = ExecutionIR::Run(r#"print('\n'.join(['a', 'b']))"#.into());

        let toml = generate_rule(&rule);
        assert!(toml.contains("run = '''"));
        assert!(toml.contains(r#"print('\n'.join(['a', 'b']))"#));
    }

    #[test]
    fn test_generate_run_block_with_triple_quotes_falls_back() {
        // Code containing ''' cannot live in a TOML literal string.
        let mut rule = make_rule("analyze");
        rule.execution = ExecutionIR::Run("x = '''doc'''\nprint(x)".into());

        let toml = generate_rule(&rule);
        assert!(toml.contains("run = \"\"\""));
        assert!(toml.contains("x = '''doc'''"));
    }

