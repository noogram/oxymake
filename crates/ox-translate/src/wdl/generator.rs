#[cfg(test)]
mod tests {
    use super::*;

    fn make_rule(name: &str) -> RuleIR {
        RuleIR {
            name: name.into(),
            inputs: vec![],
            outputs: vec![],
            execution: ExecutionIR::None,
            threads: None,
            resources: vec![],
            environment: None,
            wildcard_constraints: vec![],
            params: vec![],
            log: vec![],
            source_line: None,
            expand: None,
        }
    }

    fn make_ir(rules: Vec<RuleIR>) -> WorkflowIR {
        WorkflowIR {
            rules,
            config_file: None,
            includes: vec![],
            diagnostics: vec![],
            config_values: vec![],
            escalations: vec![],
            global_container: None,
            global_report: None,
        }
    }

    #[test]
    fn test_simple_task_generation() {
        let mut rule = make_rule("process");
        rule.inputs = vec![PortIR {
            name: Some("input_file".into()),
            pattern: "{input_file}".into(),
            lifecycle: None,
        }];
        rule.outputs = vec![PortIR {
            name: Some("result".into()),
            pattern: "output.txt".into(),
            lifecycle: None,
        }];
        rule.execution = ExecutionIR::Shell("sort {input_file} > output.txt".into());

        let wdl = generate_wdl(&make_ir(vec![rule]));
        assert!(wdl.contains("version 1.0"));
        assert!(wdl.contains("task process {"));
        assert!(wdl.contains("File input_file"));
        assert!(wdl.contains("File result = \"output.txt\""));
        assert!(wdl.contains("sort ~{input_file} > output.txt"));
    }

    #[test]
    fn test_runtime_generation() {
        let mut rule = make_rule("heavy");
        rule.execution = ExecutionIR::Shell("run_job".into());
        rule.threads = Some(ThreadsIR::Literal(8));
        rule.resources = vec![
            ResourceIR {
                key: "mem".into(),
                value: "16G".into(),
            },
            ResourceIR {
                key: "disk".into(),
                value: "100G".into(),
            },
        ];
        rule.environment = Some(EnvironmentIR::Container("docker://ubuntu:22.04".into()));

        let wdl = generate_task(&rule);
        assert!(wdl.contains("cpu: 8"));
        assert!(wdl.contains("memory: \"16 GB\""));
        assert!(wdl.contains("disks: \"local-disk 100 SSD\""));
        assert!(wdl.contains("docker: \"ubuntu:22.04\""));
    }

    #[test]
    fn test_workflow_generation() {
        let mut rule_a = make_rule("step_a");
        rule_a.execution = ExecutionIR::Shell("echo a".into());
        rule_a.outputs = vec![PortIR {
            name: Some("out".into()),
            pattern: "a.txt".into(),
            lifecycle: None,
        }];

        let mut rule_b = make_rule("step_b");
        rule_b.execution = ExecutionIR::Shell("echo b".into());
        rule_b.inputs = vec![PortIR {
            name: Some("in_b".into()),
            pattern: "{in_b}".into(),
            lifecycle: None,
        }];

        let wdl = generate_wdl(&make_ir(vec![rule_a, rule_b]));
        assert!(wdl.contains("workflow generated_workflow {"));
        assert!(wdl.contains("call step_a"));
        assert!(wdl.contains("call step_b"));
    }

    #[test]
    fn test_scatter_generation() {
        let mut rule = make_rule("process");
        rule.inputs = vec![PortIR {
            name: Some("sample".into()),
            pattern: "{sample}".into(),
            lifecycle: None,
        }];
        rule.execution = ExecutionIR::Shell("process {sample}".into());
        rule.expand = Some("zip".to_string());

        let wdl = generate_wdl(&make_ir(vec![rule]));
        assert!(wdl.contains("scatter (sample in sample_array)"));
        assert!(wdl.contains("call process"));
    }

    #[test]
    fn test_placeholder_conversion() {
        assert_eq!(convert_placeholders_to_wdl("{sample}"), "~{sample}");
        assert_eq!(
            convert_placeholders_to_wdl("echo {name} > {output}"),
            "echo ~{name} > ~{output}"
        );
        // Bash variables should be preserved
        assert_eq!(convert_placeholders_to_wdl("{1+2}"), "{1+2}");
    }

    #[test]
    fn test_mem_conversion() {
        assert_eq!(convert_mem_to_wdl("16G"), "16 GB");
        assert_eq!(convert_mem_to_wdl("500M"), "500 MB");
    }

    #[test]
    fn test_disk_conversion() {
        assert_eq!(convert_disk_to_wdl("100G"), "local-disk 100 SSD");
        assert_eq!(convert_disk_to_wdl("2048M"), "local-disk 2 SSD");
    }

    #[test]
    fn test_includes() {
        let ir = WorkflowIR {
            rules: vec![],
            config_file: None,
            includes: vec!["rules/align.toml".into()],
            diagnostics: vec![],
            config_values: vec![],
            escalations: vec![],
            global_container: None,
            global_report: None,
        };

        let wdl = generate_wdl(&ir);
        assert!(wdl.contains("import \"rules/align.wdl\""));
    }

    #[test]
    fn test_params_as_inputs() {
        let mut rule = make_rule("gen");
        rule.params = vec![
            ParamIR {
                name: "n_rows".into(),
                value: "100".into(),
            },
            ParamIR {
                name: "label".into(),
                value: "test".into(),
            },
        ];
        rule.execution = ExecutionIR::Shell("echo {n_rows}".into());

        let wdl = generate_task(&rule);
        assert!(wdl.contains("Int n_rows"));
        assert!(wdl.contains("String label"));
    }

    #[test]
    fn test_conda_warning() {
        let mut rule = make_rule("step");
        rule.environment = Some(EnvironmentIR::Conda("envs/tools.yaml".into()));
        rule.execution = ExecutionIR::Shell("echo".into());

        let wdl = generate_task(&rule);
        assert!(wdl.contains("# NOTE: Conda env"));
    }

    #[test]
    fn test_run_block_as_python() {
        let mut rule = make_rule("analyze");
        rule.execution = ExecutionIR::Run("import json\nprint('hello')".into());

        let wdl = generate_task(&rule);
        assert!(wdl.contains("python3 -c"));
        assert!(wdl.contains("import json"));
    }
}
